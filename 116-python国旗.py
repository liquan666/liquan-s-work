import turtle
turtle.setup(650,350,200,200)
turtle.penup()
turtle.fd(-150)
turtle.seth(-90)
turtle.fd(100)
turtle.pendown()
turtle.pensize(2)
turtle.hideturtle()
turtle.pencolor("red")
turtle.fillcolor("red")
turtle.begin_fill()
turtle.goto(-150,100)
turtle.goto(200,100)
turtle.goto(200,-100)
turtle.goto(-150,-100)
turtle.end_fill()
turtle.penup()
turtle.goto(-125,50)
turtle.seth(0)
turtle.pensize(1)
turtle.pencolor("yellow")
turtle.pendown()
turtle.fillcolor("yellow")
turtle.begin_fill()
for i in range(5):
    turtle.forward(75)
    turtle.right(144)
turtle.end_fill()
turtle.penup()
turtle.goto(-25,85)
turtle.seth(-30)
turtle.pensize(1)
turtle.pencolor("yellow")
turtle.pendown()
turtle.fillcolor("yellow")
turtle.begin_fill()
for i in range(5):
     turtle.forward(20)              
     turtle.right(144)              
turtle.end_fill()
turtle.penup()
turtle.goto(0,50)
turtle.seth(30)
turtle.pensize(1)
turtle.pencolor("yellow")
turtle.pendown()
turtle.fillcolor("yellow")
turtle.begin_fill()
for i in range(5):
    turtle.forward(20)
    turtle.right(144)
turtle.end_fill()    
turtle.penup()
turtle.goto(0,20)
turtle.seth(5)
turtle.pensize(1)
turtle.pencolor("yellow")
turtle.pendown()                  
turtle.fillcolor("yellow")    
turtle.begin_fill()
for i in range(5):
    turtle.forward(20)
    turtle.right(144)
turtle.end_fill()
turtle.penup()
turtle.goto(-25,-5)
turtle.seth(-25)
turtle.pensize(1)
turtle.pencolor("yellow")
turtle.pendown()
turtle.fillcolor("yellow")
turtle.begin_fill()
for i in range(5):
    turtle.forward(20)
    turtle.right(144)
turtle.end_fill()
turtle.done()
